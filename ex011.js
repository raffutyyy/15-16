let pergunta = prompt('Qual turno voce estuda? digite M (matutino) ou V (Vespertino) ou N (Noturno)')
switch(pergunta){
    case 'M':
        console.log('Bom dia!')
        break
    case 'V':
        console.log('Boa tarde!')
        break
    case 'N':
        console.log('Boa noite!')
        break
}
